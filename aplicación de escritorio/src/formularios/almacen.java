    private void mostrar() {
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Database.getTabla(" select productos.codigo_de_barras, nombre_productos, marca,contenido,precio,cantidad,stock from productos inner join almacen on productos.codigo_de_barras = almacen.codigo_de_barras");
        modelo.setColumnIdentifiers(new Object[]{"codigo barras","producto","marca","contenido", "precio","stock"});
        try {
            while (rs.next()) {
                // a√±ade los resultado a al modelo de tabla
                modelo.addRow(new Object[]{rs.getString("codigo_de_barras"), 
                    rs.getString("nombre_productos"), 
                    rs.getString("marca"),
                    rs.getString("contenido"), 
                    rs.getString("precio"), 
                    rs.getInt("stock")});
            }
            // asigna el modelo a la tabla
            jt_tabla_almacen.setModel(modelo);
        } catch (Exception e) {
            System.out.println(e);
        }

    }
    

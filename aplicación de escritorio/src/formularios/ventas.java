    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        
        try{ 
            int id=Integer.parseInt(this.jtf_codigo1.getText());
            if (id == 0){
                String nombre=this.jtf_producto1.getText(); 
                float precio=Float.parseFloat(this.jtf_precio1.getText()); 
                float cantidad=Float.parseFloat(this.jtf_cantidad.getText());
                float total=Float.parseFloat(this.jtf_total1.getText());
                
                st.executeUpdate("Insert into almacen_pedidos (nombre,cantidad,total) values ("+nombre+"','"+precio+"','"+cantidad+"',"+total+");");  
                JOptionPane.showMessageDialog(null,"Venta realizada");
            }
            else {
                
                String nombre=this.jtf_producto1.getText(); 
                float precio=Float.parseFloat(this.jtf_precio1.getText()); 
                float cantidad=Float.parseFloat(this.jtf_cantidad.getText());
                float total=Float.parseFloat(this.jtf_total1.getText());
                float tot = cantidad*precio; 
                this.jtf_total1.setText(String.valueOf(total));        
                st.executeUpdate("UPDATE pedidos_almacen SET nombre= '"+ nombre +"' ,precio='"+precio+"',cantidad='"+cantidad+"',total='"+tot+"' WHERE ID='"+id+"';");
                   JOptionPane.showMessageDialog(this,"El registro se modifico correctamente");
            }
        } catch(Exception err) 
        { 
            JOptionPane.showMessageDialog(null,"Error "+err.getMessage()); 
        } 
        
        Conectar();
        limpiar();
    }//GEN-LAST:event_jButton2MouseClicked


    private void jtf_buscartodoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtf_buscartodoKeyPressed

        String[] titulos = {"ID","nombre producto","precio","cantidad","Total","fecha"};
        String[] registros = new String[50];

        String sql = "select ID,nombre,cantidad,total,precio,fecha from almacen_pedidos WHERE ID LIKE '%" + jtf_buscartodo.getText() + "%' "
                + "OR nombre LIKE '%" + jtf_buscartodo.getText() + "%'"
                + "OR cantidad  LIKE '%" + jtf_buscartodo.getText() + "%'"
                + "OR precio LIKE '%" + jtf_buscartodo.getText() + "%'"
                + "OR total LIKE '%" + jtf_buscartodo.getText() + "%'"
                + "OR fecha LIKE '%" + jtf_buscartodo.getText() + "%'";
        model = new DefaultTableModel(null, titulos);
        Database cc = new Database();
        Connection cn = getConexion();
        try {
            Statement st = (Statement) cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("ID");
                registros[1] = rs.getString("nombre");
                registros[3] = rs.getString("cantidad");
                registros[4] = rs.getString("precio");
                registros[5] = rs.getString("total");
                registros[6] = rs.getString("fecha");
                model.addRow(registros);
            }
            jt_ventas.setModel(model);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
       
    }//GEN-LAST:event_jtf_buscartodoKeyPressed


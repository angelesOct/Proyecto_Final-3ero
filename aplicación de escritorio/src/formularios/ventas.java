    private void mostrar() {
        DefaultTableModel modelo = new DefaultTableModel();
        ResultSet rs = Database.getTabla("select ID,nombre,cantidad,total from almacen_pedidos");
        modelo.setColumnIdentifiers(new Object[]{"ID","nombre producto","cantidad","Total"});
        try {
            while (rs.next()) {
                // a√±ade los resultado a al modelo de tabla
                modelo.addRow(new Object[]{
                    rs.getInt("ID"), 
                    rs.getString("nombre"),
                    rs.getFloat("cantidad"), 
                    rs.getFloat("Total")});                                    
            }
            // asigna el modelo a la tabla
            jt_ventas.setModel(modelo);
        } catch (Exception e) {
            System.out.println(e);
        }

    }
    

    private void jtf_buscartodoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtf_buscartodoKeyPressed
        
        String[] titulos = {"codigo barras","producto","marca","contenido", "precio","stock"};
        String[] registros = new String[50];

        String sql = "select  productos.codigo_de_barras, productos.nombre_productos, productos.marca,produtos.contenido,precio,stock from productos inner join almacen on productos.codigo_prod = almacen.codigo_prod WHERE codigo_de_barras LIKE '%" + jtf_buscartodo.getText() + "%' "
                + "OR nombre_productos LIKE '%" + jtf_buscartodo.getText() + "%'"
                + "OR marca LIKE '%" + jtf_buscartodo.getText() + "%'";
        model = new DefaultTableModel(null, titulos);
        Database cc = new Database();
        Connection cn = getConexion();
        try {
            Statement st = (Statement) cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                registros[0] = rs.getString("codigo_de_barras");
                registros[1] = rs.getString("nombre_productos");
                registros[2] = rs.getString("marca");
                registros[3] = rs.getString("contenido");
                registros[4] = rs.getString("precio");
                registros[5] = rs.getString("stock");
                model.addRow(registros);
            }
            jt_tabla_almacen.setModel(model);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }//GEN-LAST:event_jtf_buscartodoKeyPressed

